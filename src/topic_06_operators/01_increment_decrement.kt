package topic_06_operators

fun main() {
    // postfix form -> Change the value of a variable after it is used.
    var number = 1
    number++
    println(number)
    number--
    println(number)

     // postfix form -> Change the value of a variable before it is used.
    var num = 1
    ++num
    println(num)
    --num
    println(num)



}