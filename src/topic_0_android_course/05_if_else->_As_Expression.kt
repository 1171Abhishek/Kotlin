package topic_0_android_course

fun main() {
//    If else and else if as expression
    val number = 12
    val ifElseAndElseIfExpression =
        if (number == 1) "Twelve"
        else if (number == 2) "Two"
        else "Three"
    println(ifElseAndElseIfExpression)

//    Shorthand if else
    val shorthandTry = if (1 == 2) "False" else "True"
    println(shorthandTry)
}